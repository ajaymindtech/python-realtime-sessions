# 1. Creating a set
fruits = {"apple", "banana", "cherry"}
print("Fruits set:", fruits)

# 2. Adding elements
fruits.add("orange")
print("After adding orange:", fruits)

# 3. Removing elements
fruits.remove("banana")
print("After removing banana:", fruits)

# 4. Checking membership
print("Is apple in fruits?", "apple" in fruits)
